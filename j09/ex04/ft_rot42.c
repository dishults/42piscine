char	*ft_rot42(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 'a' && str[i] <= 'z')
		|| (str[i] >= 'A' && str[i] <= 'Z'))
		{
			if ((str[i] >= 'k' && str[i] <= 'z')
			|| (str[i] >= 'K' && str[i] <= 'Z'))
				str[i] -= 10;
			else
				str[i] += 16;
			i++;
		}
		else
			i++;
	}
	return (str);
}
