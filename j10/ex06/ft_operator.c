#include "headers.h"

int	ft_operator(int v1, int v2, char **av, void (*f)(int))
{
	if (av[2][0] == '+')
		f(v1 + v2);
	else if (av[2][0] == '-')
		f(v1 - v2);
	else if (av[2][0] == '*')
		f(v1 * v2);
	else if (av[2][0] == '/')
		f(v1 / v2);
	else if (av[2][0] == '%')
		f(v1 % v2);
	return (0);
}
