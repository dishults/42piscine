#include "headers.h"

int	ft_add(int v1, int v2)
{
	return (v1 + v2);
}

int	ft_sub(int v1, int v2)
{
	return (v1 - v2);
}

int	ft_mul(int v1, int v2)
{
	return (v1 * v2);
}

int	ft_div(int v1, int v2)
{
	return (v1 / v2);
}

int	ft_mod(int v1, int v2)
{
	return (v1 % v2);
}
